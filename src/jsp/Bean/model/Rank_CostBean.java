package jsp.Bean.model;

public class Rank_CostBean {
	private String rank;
	private int compensation;
	private int expend_sure;
	private int expend_coop;
	
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public int getCompensation() {
		return compensation;
	}
	public void setCompensation(int compensation) {
		this.compensation = compensation;
	}
	public int getExpend_sure() {
		return expend_sure;
	}
	public void setExpend_sure(int expend_sure) {
		this.expend_sure = expend_sure;
	}
	public int getExpend_coop() {
		return expend_coop;
	}
	public void setExpend_coop(int expend_coop) {
		this.expend_coop = expend_coop;
	}
	
}
