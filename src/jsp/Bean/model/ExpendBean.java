package jsp.Bean.model;

public class ExpendBean {
	private String team;
	private float first_Expend;
	private float second_Expend;
	private float total_Expend;
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public float getFirst_Expend() {
		return first_Expend;
	}
	public void setFirst_Expend(float first_Expend) {
		this.first_Expend = first_Expend;
	}
	public float getSecond_Expend() {
		return second_Expend;
	}
	public void setSecond_Expend(float second_Expend) {
		this.second_Expend = second_Expend;
	}
	public float getTotal_Expend() {
		return total_Expend;
	}
	public void setTotal_Expend(float total_Expend) {
		this.total_Expend = total_Expend;
	}
	
}
